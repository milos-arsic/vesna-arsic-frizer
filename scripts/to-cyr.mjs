const map = {
  a: "а", b: "б", v: "в", g: "г", d: "д", đ: "ђ", e: "е", ž: "ж", z: "з",
  i: "и", j: "ј", k: "к", l: "л", m: "м", n: "н", o: "о", p: "п", r: "р",
  s: "с", t: "т", u: "у", f: "ф", h: "х", c: "ц", č: "ч", ć: "ћ", š: "ш",
  A: "А", B: "Б", V: "В", G: "Г", D: "Д", Đ: "Ђ", E: "Е", Ž: "Ж", Z: "З",
  I: "И", J: "Ј", K: "К", L: "Л", M: "М", N: "Н", O: "О", P: "П", R: "Р",
  S: "С", T: "Т", U: "У", F: "Ф", H: "Х", C: "Ц", Č: "Ч", Ć: "Ћ", Š: "Ш",
};

export function toCyr(text) {
  return text.replace(/[A-Za-zčćđšžČĆĐŠŽ]/g, (ch) => map[ch] ?? ch);
}
